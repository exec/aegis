/* -- Framebuffer state ----------------------------------------------------- */
int fb_available = 0;
static int s_fb_locked = 0;  /* 1 = compositor owns FB, suppress text output */

static volatile uint32_t *s_fb_va    = (void *)0;
/* Shadow buffer: normal WB-cached DRAM copy of the framebuffer.
 * All pixel writes go to both s_shadow and s_fb_va.
 * _fb_scroll reads from s_shadow (fast cacheable read), then blits
 * the whole screen to s_fb_va as sequential WC writes (fast write-combining).
 * This avoids uncached reads from WC framebuffer memory during scroll,
 * which is the same strategy Linux vesafb/efifb uses. */
static uint32_t          *s_shadow   = (void *)0;
static uint32_t           s_pitch_px = 0;
static uint32_t           s_cols     = 0;
static uint32_t           s_rows     = 0;
static uint32_t           s_col      = 0;
static uint32_t           s_row      = 0;

/* ANSI CSI escape-sequence state machine.
 * Handles the two sequences emitted by /bin/clear: \033[2J and \033[H. */
static int  s_esc_state = 0;    /* 0=normal 1=saw_ESC 2=in_CSI */
static char s_esc_buf[16];
static int  s_esc_len   = 0;

#define FB_FG  0x00FFFFFFu
#define FB_BG  0x00000000u

static void
_fb_draw_char(uint32_t row, uint32_t col, char c)
{
    const uint8_t *glyph = s_font + ((uint8_t)c) * 16u;
    uint32_t base_px = row * 16u * s_pitch_px + col * 8u;
    uint32_t y, x;
    for (y = 0; y < 16u; y++) {
        uint8_t bits = glyph[y];
        for (x = 0; x < 8u; x++) {
            uint32_t color = (bits & (0x80u >> x)) ? FB_FG : FB_BG;
            uint32_t idx   = base_px + y * s_pitch_px + x;
            if (s_shadow) s_shadow[idx] = color;
            s_fb_va[idx] = color;
        }
    }
}

static void
_fb_scroll(void)
{
    uint32_t row_pixels = 16u * s_pitch_px;
    uint32_t total      = (s_rows - 1u) * row_pixels;

    if (s_shadow) {
        /* Fast path: shift shadow in DRAM (WB-cached, fast reads),
         * then blit the whole screen to the WC framebuffer as a single
         * sequential write stream (write-combined, fast).
         * FB_BG == 0x00000000, so memset-zero clears the last text row. */
        __builtin_memmove(s_shadow, s_shadow + row_pixels,
                          total * sizeof(uint32_t));
        __builtin_memset(s_shadow + (s_rows - 1u) * row_pixels, 0,
                         row_pixels * sizeof(uint32_t));
        /* Cast away volatile: we want REP MOVSQ batched WC writes, not
         * per-element volatile stores which prevent write-combining. */
        __builtin_memmove((uint32_t *)s_fb_va, s_shadow,
                          s_rows * row_pixels * sizeof(uint32_t));
    } else {
        /* Slow fallback (no shadow): scalar loop over WC reads */
        uint32_t i;
        for (i = 0; i < total; i++)
            s_fb_va[i] = s_fb_va[i + row_pixels];
        for (i = 0; i < row_pixels; i++)
            s_fb_va[(s_rows - 1u) * row_pixels + i] = FB_BG;
    }
    s_row = s_rows - 1u;
}

/* Physical address and dimensions — saved for sys_fb_map */
static uint64_t s_fb_phys;
static uint32_t s_fb_width;
static uint32_t s_fb_height;
static uint32_t s_fb_pitch;

int
fb_get_phys_info(uint64_t *phys_out, uint32_t *width_out,
                 uint32_t *height_out, uint32_t *pitch_out)
{
    if (!fb_available || s_fb_phys == 0)
        return 0;
    *phys_out  = s_fb_phys;
    *width_out = s_fb_width;
    *height_out = s_fb_height;
    *pitch_out = s_fb_pitch;
    return 1;
}

void
fb_init(void)
{
    arch_fb_info_t info;
    uint32_t fb_pages, i;
    uint8_t *region;

    if (!arch_get_fb_info(&info))
        return;

    fb_pages = (info.pitch * info.height + 0xFFFu) / 0x1000u;
    region   = (uint8_t *)kva_alloc_pages((uint64_t)fb_pages);
    if (!region) {
        serial_write_string("[FB] WARN: kva alloc failed\n");
        return;
    }

    for (i = 0; i < fb_pages; i++) {
        uint64_t va     = (uint64_t)region + (uint64_t)i * 0x1000u;
        uint64_t old_pa = kva_page_phys((void *)va);
        /* kva_alloc_pages already mapped va to a PMM frame.
         * SAFETY: vmm_unmap_page removes the existing PTE so that the
         * following vmm_map_page does not panic on a double-map.
         * The PMM frame is freed back to the allocator. */
        vmm_unmap_page(va);
        pmm_free_page(old_pa);
        vmm_map_page(va,
                     info.addr + (uint64_t)i * 0x1000u,
                     VMM_FLAG_PRESENT | VMM_FLAG_WRITABLE | VMM_FLAG_WC);
    }

    s_fb_va    = (volatile uint32_t *)region;
    s_pitch_px = info.pitch / 4u;
    s_cols     = info.width  / 8u;
    s_rows     = info.height / 16u;

    /* Save physical info for sys_fb_map */
    s_fb_phys   = info.addr;
    s_fb_width  = info.width;
    s_fb_height = info.height;
    s_fb_pitch  = info.pitch;
    s_col      = 0;
    s_row      = 0;

    /* Allocate shadow buffer: same size as framebuffer, backed by normal
     * WB-cached PMM pages (no remapping needed — kva_alloc_pages uses WB). */
    {
        uint8_t *shbuf = (uint8_t *)kva_alloc_pages((uint64_t)fb_pages);
        if (shbuf)
            s_shadow = (uint32_t *)shbuf;
        /* Shadow allocation failure is non-fatal; scroll falls back to
         * the slow scalar loop. */
    }

    {
        uint32_t total_px = s_pitch_px * info.height;
        /* FB_BG == 0x00000000; memset-zero clears to black.
         * Cast away volatile for batched write-combined writes. */
        __builtin_memset((uint32_t *)s_fb_va, 0, total_px * sizeof(uint32_t));
        /* Zero the shadow too.  Without this, gutter pixels (right of the last
         * character column, between s_cols*8 and s_pitch_px) are PMM garbage.
         * On the first _fb_scroll those garbage pixels are blitted to the FB,
         * causing "TV static" on the right side of the screen.
         *
         * SAFETY: the multiboot2 info structure is reserved in PMM via
         * arch_mm.c x86_reserved[1] (set at arch_mm_init time), so
         * kva_alloc_pages cannot return a page that overlaps the RSDP. */
        if (s_shadow)
            __builtin_memset(s_shadow, 0, total_px * sizeof(uint32_t));
    }

    fb_available = 1;
    printk("[FB] OK: %ux%ux%u\n", info.width, info.height, (uint32_t)info.bpp);
}

static void
_fb_clear_screen(void)
{
    uint32_t total_px = s_pitch_px * s_rows * 16u;
    __builtin_memset((uint32_t *)s_fb_va, 0, total_px * sizeof(uint32_t));
    if (s_shadow)
        __builtin_memset(s_shadow, 0, total_px * sizeof(uint32_t));
    s_row = 0;
    s_col = 0;
}

/* _fb_dispatch_csi — called when a CSI sequence final-byte is received.
 * s_esc_buf[0..s_esc_len-1] holds the parameter string; cmd is the
 * final byte ('J', 'H', 'K', …). */
static void
_fb_dispatch_csi(char cmd)
{
    if (cmd == 'J') {
        /* Erase in display.  Only param=2 (whole screen) is handled. */
        if (s_esc_len == 1 && s_esc_buf[0] == '2')
            _fb_clear_screen();
        /* params 0, 1, or anything else: no-op for now */
    } else if (cmd == 'H' || cmd == 'f') {
        /* Cursor position: \033[row;colH (1-indexed, defaults to 1). */
        uint32_t row = 0, col = 0;
        int i = 0;
        /* Parse first number (row) */
        while (i < s_esc_len && s_esc_buf[i] >= '0' && s_esc_buf[i] <= '9')
            row = row * 10u + (uint32_t)(s_esc_buf[i++] - '0');
        if (row > 0) row--;  /* convert 1-indexed to 0-indexed */
        if (i < s_esc_len && s_esc_buf[i] == ';') {
            i++;
            while (i < s_esc_len && s_esc_buf[i] >= '0' && s_esc_buf[i] <= '9')
                col = col * 10u + (uint32_t)(s_esc_buf[i++] - '0');
            if (col > 0) col--;
        }
        s_row = (row < s_rows) ? row : s_rows - 1u;
        s_col = (col < s_cols) ? col : s_cols - 1u;
    }
    /* All other sequences are silently ignored. */
}

void
fb_putchar(char c)
{
    if (!fb_available || s_fb_locked) return;

    /* ANSI CSI state machine */
    if (s_esc_state == 1) {
        if (c == '[') {
            s_esc_state = 2;
            s_esc_len   = 0;
        } else {
            s_esc_state = 0;
        }
        return;
    }
    if (s_esc_state == 2) {
        if ((c >= '0' && c <= '9') || c == ';') {
            if (s_esc_len < 15)
                s_esc_buf[s_esc_len++] = c;
            return;
        }
        s_esc_state     = 0;
        s_esc_buf[s_esc_len] = '\0';
        _fb_dispatch_csi(c);
        return;
    }
    if (c == '\033') {
        s_esc_state = 1;
        return;
    }

    if (c == '\n') {
        s_col = 0;
        s_row++;
    } else if (c == '\r') {
        s_col = 0;
    } else if (c == '\b') {
        if (s_col > 0) {
            s_col--;
            _fb_draw_char(s_row, s_col, ' ');
        }
    } else {
        _fb_draw_char(s_row, s_col, c);
        s_col++;
        if (s_col >= s_cols) {
            s_col = 0;
            s_row++;
        }
    }

    if (s_row >= s_rows)
        _fb_scroll();
}

void
fb_write_string(const char *s)
{
    while (*s)
        fb_putchar(*s++);
}

void
fb_lock_compositor(void)
{
    s_fb_locked = 1;
}

void
fb_check_amd(void)
{
    const pcie_device_t *amd;

    if (fb_available)
        return;   /* framebuffer already working — nothing to report */

    /* Note: pcie_find_device returns the first class-0x03 device.
     * On multi-GPU systems this may not be the AMD GPU if another
     * display controller appears first in the PCIe table. Single-iGPU
     * systems (the target platform) are always correct. */
    amd = pcie_find_device(0x03u, 0xFFu, 0xFFu);
    if (amd == NULL || amd->vendor_id != 0x1002u)
        return;   /* no AMD display device — silent */

    printk("[FB] WARN: AMD GPU 0x%x found but no framebuffer tag from GRUB\n",
           (uint32_t)amd->device_id);
    printk("[FB] WARN: ensure GRUB boots in UEFI mode (AMD APUs lack VBE)\n");
}

