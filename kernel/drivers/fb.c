/* -- Framebuffer state ----------------------------------------------------- */
int fb_available = 0;

static volatile uint32_t *s_fb_va    = (void *)0;
static uint32_t           s_pitch_px = 0;
static uint32_t           s_cols     = 0;
static uint32_t           s_rows     = 0;
static uint32_t           s_col      = 0;
static uint32_t           s_row      = 0;

#define FB_FG  0x00FFFFFFu
#define FB_BG  0x00000000u

static void
_fb_draw_char(uint32_t row, uint32_t col, char c)
{
    const uint8_t *glyph = s_font + ((uint8_t)c) * 16u;
    uint32_t base_px = row * 16u * s_pitch_px + col * 8u;
    uint32_t y, x;
    for (y = 0; y < 16u; y++) {
        uint8_t bits = glyph[y];
        for (x = 0; x < 8u; x++) {
            s_fb_va[base_px + y * s_pitch_px + x] =
                (bits & (0x80u >> x)) ? FB_FG : FB_BG;
        }
    }
}

static void
_fb_scroll(void)
{
    uint32_t row_pixels = 16u * s_pitch_px;
    uint32_t total      = (s_rows - 1u) * row_pixels;
    uint32_t i;
    for (i = 0; i < total; i++)
        s_fb_va[i] = s_fb_va[i + row_pixels];
    for (i = 0; i < row_pixels; i++)
        s_fb_va[(s_rows - 1u) * row_pixels + i] = FB_BG;
    s_row = s_rows - 1u;
}

void
fb_init(void)
{
    arch_fb_info_t info;
    uint32_t fb_pages, i;
    uint8_t *region;

    if (!arch_get_fb_info(&info))
        return;

    fb_pages = (info.pitch * info.height + 0xFFFu) / 0x1000u;
    region   = (uint8_t *)kva_alloc_pages((uint64_t)fb_pages);
    if (!region) {
        serial_write_string("[FB] WARN: kva alloc failed\n");
        return;
    }

    for (i = 0; i < fb_pages; i++) {
        uint64_t va     = (uint64_t)region + (uint64_t)i * 0x1000u;
        uint64_t old_pa = kva_page_phys((void *)va);
        /* kva_alloc_pages already mapped va to a PMM frame.
         * SAFETY: vmm_unmap_page removes the existing PTE so that the
         * following vmm_map_page does not panic on a double-map.
         * The PMM frame is freed back to the allocator. */
        vmm_unmap_page(va);
        pmm_free_page(old_pa);
        vmm_map_page(va,
                     info.addr + (uint64_t)i * 0x1000u,
                     VMM_FLAG_PRESENT | VMM_FLAG_WRITABLE | VMM_FLAG_WC);
    }

    s_fb_va    = (volatile uint32_t *)region;
    s_pitch_px = info.pitch / 4u;
    s_cols     = info.width  / 8u;
    s_rows     = info.height / 16u;
    s_col      = 0;
    s_row      = 0;

    {
        uint32_t total_px = (info.pitch / 4u) * info.height;
        for (i = 0; i < total_px; i++)
            s_fb_va[i] = FB_BG;
    }

    fb_available = 1;
    printk("[FB] OK: %ux%ux%u\n", info.width, info.height, (uint32_t)info.bpp);
}

void
fb_putchar(char c)
{
    if (!fb_available) return;

    if (c == '\n') {
        s_col = 0;
        s_row++;
    } else if (c == '\r') {
        s_col = 0;
    } else if (c == '\b') {
        if (s_col > 0) {
            s_col--;
            _fb_draw_char(s_row, s_col, ' ');
        }
    } else {
        _fb_draw_char(s_row, s_col, c);
        s_col++;
        if (s_col >= s_cols) {
            s_col = 0;
            s_row++;
        }
    }

    if (s_row >= s_rows)
        _fb_scroll();
}

void
fb_write_string(const char *s)
{
    while (*s)
        fb_putchar(*s++);
}

void
fb_check_amd(void)
{
    const pcie_device_t *amd;

    if (fb_available)
        return;   /* framebuffer already working — nothing to report */

    /* pcie_find_device wildcard: class=0x03 (Display), subclass/progif=0xFF */
    amd = pcie_find_device(0x03u, 0xFFu, 0xFFu);
    if (amd == NULL || amd->vendor_id != 0x1002u)
        return;   /* no AMD display device — silent */

    printk("[FB] WARN: AMD GPU 0x%x found but no UEFI framebuffer tag\n",
           (uint32_t)amd->device_id);
    printk("[FB] WARN: boot Aegis in UEFI mode for AMD iGPU display\n");
}
